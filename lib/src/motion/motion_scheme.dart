/// Motion schemes following Material Design 3 specifications.
///
/// - [expressive]: Dynamic, bouncy animations for engaging UX
/// - [standard]: Balanced animations for general use
/// - [reduced]: Minimal/instant animations for accessibility
///   (used when platform "Reduce Motion" is enabled)
enum MotionScheme { expressive, standard, reduced }

enum MotionSpeed { fast, defaultSpeed, slow }

enum MotionType { spatial, effects }
